 <script>  //---------------------------------- SLIDER ROTATE------------------ //
	$(window).load(function() {
  	  var slider = ('#slider'),		//main division
      imgs = $('#slider div'),		// all divisions inside that
      sliderWidth = 0,
      windowWidth = $(window).width(), // screen width
      startImg,
      startPos,
      prev = $('#prev'),	// next prev button functions
      next = $('#next'),	// next prev button functions
      totalSlides = 4,
      clickTotal = 0;

	  for (var i = 0; i < imgs.length; i++) {
	   sliderWidth += $(imgs[i]).outerWidth(); // get width all of images
	  }
		 imgs.filter(':first').addClass('start');
		 startImg = $('.start').outerWidth();
		 startPos = -((sliderWidth * 2) - (windowWidth / 2) + (startImg / 2)); // calculate starting position

		 $('.slider-div').css('overflow', 'hidden'); // hide all images outside of main division

		 $(slider).css('width', sliderWidth * 10); //make main division more larger

		 imgs.filter(':first').before(imgs.clone());
		 imgs.filter(':first').before(imgs.clone()); //Clone images first after last
		 imgs.filter(':last').after(imgs.clone());

		 $(slider).css('left', startPos); // get the position to the calculated start position


		 prev.click(function(e) {  //Previous click
		   e.preventDefault();
		   clickTotal--;
		   $(slider).animate({
		     left: '+=' + sliderWidth / totalSlides * 2
		   }, 1000, function() {
		     if (clickTotal == -totalSlides) {
		       clickTotal = 0;
		       $(slider).css('left', startPos);
		     }
		   });
		 });


		 next.click(function(e) { //Next click
		   e.preventDefault();
		   clickTotal++;
		   $(slider).animate({
		     left: '-=' + sliderWidth / totalSlides * 2
		   }, 1000, function() {
		     if (clickTotal == totalSlides) {
		       clickTotal = 0;
		       $(slider).css('left', startPos);
		     }
		   });
		 });

		});
</script>

<script>// -------------------------------------- 2nd slider ---------------------//
	var width =$(window).width();
	let slideIndex;
	if(width<768){		// If smalll screen
		slideIndex = 1;
		showSlides(slideIndex);
	}
	
	$( window ).resize(function() { // when resize screen
		width = $(window).width(); 
		if(width<768){	// if screen is a mobile
		slideIndex = 1;
		showSlides(slideIndex);
		}
		else{	// if screen is large, division will devide as 3 parts
			$('.mySlides').css('display','inline-block');
			$('.mySlides').css('width','33%');

		}

	});
	

	function plusSlides(n) { 		
	  showSlides(slideIndex += n);
	}

	function currentSlide(n) { 
	  showSlides(slideIndex = n);
	}

	function showSlides(n) {
	  var i;
	  var slides = $(".mySlides");
	  var dots = $(".dot");
	  if (n > slides.length) {slideIndex = 1} 
	  if (n < 1) {slideIndex = slides.length}
	  for (i = 0; i < slides.length; i++) {
	      slides[i].style.display = "none";  
	  }
	  for (i = 0; i < dots.length; i++) {
	      dots[i].className = dots[i].className.replace(" active-dot", "");
	  }
	  slides[slideIndex-1].style.display = "block";  
	  dots[slideIndex-1].className += " active-dot";
	}

</script>

